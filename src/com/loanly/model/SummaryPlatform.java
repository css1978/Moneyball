package com.loanly.model;

public class SummaryPlatform {
	private String platform_name;
	private Double dueAllAmt;
	private Double percentage;
	private Double dueOwnAmt;
	private Double dueIns;
	private Double inIns;
	private Double inAward;
	private Double inTotal;
	private Double insRate;
	public String getPlatform_name() {
		return platform_name;
	}
	public void setPlatform_name(String platform_name) {
		this.platform_name = platform_name;
	}
	public Double getPercentage() {
		return percentage;
	}
	public void setPercentage(Double percentage) {
		this.percentage = percentage;
	}
	public Double getDueAllAmt() {
		return dueAllAmt;
	}
	public void setDueAllAmt(Double dueAllAmt) {
		this.dueAllAmt = dueAllAmt;
	}
	public Double getDueOwnAmt() {
		return dueOwnAmt;
	}
	public void setDueOwnAmt(Double dueOwnAmt) {
		this.dueOwnAmt = dueOwnAmt;
	}
	public Double getInIns() {
		return inIns;
	}
	public void setInIns(Double inIns) {
		this.inIns = inIns;
	}
	public Double getDueIns() {
		return dueIns;
	}
	public void setDueIns(Double dueIns) {
		this.dueIns = dueIns;
	}
	public Double getInAward() {
		return inAward;
	}
	public void setInAward(Double inAward) {
		this.inAward = inAward;
	}
	public Double getInTotal() {
		return inTotal;
	}
	public void setInTotal(Double inTotal) {
		this.inTotal = inTotal;
	}
	public Double getInsRate() {
		return insRate;
	}
	public void setInsRate(Double insRate) {
		this.insRate = insRate;
	}
	
	
	
}
